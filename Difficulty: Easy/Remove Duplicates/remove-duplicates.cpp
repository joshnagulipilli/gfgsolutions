class Solution {
  public:

    string removeDups(string str) {
        // Your code goes here
        string p="";
        map<char,int>mp;
        for(int i=0;str[i];i++)
        {
            if(mp[str[i]]==0)
            {
                p+=str[i];
            }
            mp[str[i]]++;
        }
        return p;
    }
};

