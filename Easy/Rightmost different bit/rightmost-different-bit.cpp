class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        int c=0,d=0;
        int i=1;
        while(m > 0 || n>0){
            c=m%2;
            m=m/2;
            d=n%2;
            n=n/2;
            if(c!=d){
               return i; 
            }
            i++;
            
        }
        return -1;
    }
};

